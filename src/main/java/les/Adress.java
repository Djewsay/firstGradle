package les;

public class Adress {

    String recipient;
    String street;
    String number;
    String city;
    String postalCode;


    public Adress(String r, String s, String n, String c, String p){

        this.recipient = r;
        this.street = s;
        this.number = n;
        this.city = c;
        this.postalCode= p;


    }



}
